<article>
    <h4 align="center">DIRANG</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Dirang" target="_blank">
    <img src="dirang.jpg" align="right" width="400px" alt=""></a>
</p>
    <p align="left">
    Snow-clad Himalayan ranges, a rustic charm and spirituality filled in the air; all these ingredients make Dirang a perfect hideout to spend some tranquil holidays in the lap of Mother Nature. The little village lies hidden in the West Kameng district of Arunachal Pradesh and stands as a gateway to the state’s much celebrated tourist destination, Tawang. This quaint hill town’s picturesque scenic beauty and simplicity of the ever smiling people is sure to take any visitor’s heart in no time. While Dirang is no less than a paradise for nature lovers, landscape photographers and adventure seekers. </p>
    <p align="left">Situated enroute to Tawang from little down of Bomdila in Arunachal Pradesh, Dirang is one of the most breath-taking travel places in North East India that is sure to cast a spell on every visitor. Engulfed in awe-inspiring Himalayan vistas sprinkled with verdant greenery, sparkling streams, enchanting waterfalls and natural hot water springs, the tiny hamlet truly makes for an idyllic getaway for nature lovers, adventure enthusiasts and cultural freaks alike. The village’s scenic trails immersed in profound tranquillity provide for a fascinating time to all the bird watchers too; venture on a nature stroll early in the morning and get treated with charming sights of white-throated redstarts, fire-tailed sunbirds, rufous-bellied tits and rainbow-coloured trogopans playing hide and seek amidst the thick bushes. For a more splendid experience, embark on a short hike to the ‘Sangti Valley’, yet another beautiful sightseeing place that lies just 7 km. off the main city centre and is famous as a wintering spot for the migratory black-necked cranes. Besides cranes, the alluring charm of the valley soaked in captivating panoramas of striking kiwi and apple orchards and lush paddy fields along the pristine Sangti River is magical enough to leave all the visitors entranced and captivated for their entire lives.
    </p>
    <p align="left">
    For those who are up for some adventure in Dirang, must make best use of the opportunity and take a ride to the famous ‘Sela Pass’ that sits 13,700 ft. above the sea level. One of the top tourist attractions in the region located just 80 km. off the main city centre, the pass stands as a gateway to Tawang and provides for an adrenaline pumping adventure snaking through winding hilly roads, dangerous curves and rough terrains. While the ride is indescribably exhilarating, it is well worth all the effort. The scenic vistas here are just to die for; the frozen ‘Sela Lake’ and clouds floating right above the heads make for a celestial scenery and leave everyone gasping in awe of nature’s unmatched craftsmanship.
    </p>
    <p align="left">
    Dirang also counts amongst the best places to visit in North East India for those interested in cultural explorations; the natives of Monpa tribe have been inhabiting in the village for more than 500 years and an interaction with them provides for a great time to avid anthropologists and sociologists have an insight into their unique lifestyles, food habits, cultures and traditions.
    </p>
</article>
