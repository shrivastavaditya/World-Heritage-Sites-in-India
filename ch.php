<article>
    <h4 align="center">RAJIM</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Rajim" target="_blank">
    <img src="rajim.jpg" width=250px align="right" alt=""></a>
</p>
    <p align="left">
    Rajim is considered the perfect place to witness Chhattisgarh’s rich cultural heritage. Also known as the ‘Prayag’ of Chhattisgarh, Rajim is the spot of the union of three holy rivers - Mahanadi (Chitrotpala), Pairi and Sondur. This point of meeting of these three rivers is also referred to as the ‘Triveni Sangam’. </p>
    <p align="left">The Chhattisgarh Kumbh Mela takes place over here from Magh Poornima to Mahashivaratri every year. Pilgrims come together from several places across the country to take part in this auspicious event. The town is also home to various historical monuments that attract prominent historians. 
    </p>
    
</article>
