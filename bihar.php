    <h4 align="center">NALANDA UNIVERSITY</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Nalanda" target="_blank">
    <img src="nal.jpg" align="right" width="400px" alt=""></a>
</p>
    <p align="left">
    Nalanda is the name of an ancient center of higher learning inBihar, India. The site of Nalanda is located in the Indian state of Bihar, about 55 miles south east of Patna, and was a Buddhist center of learning from 427 to 1197 CE. It has been called “one of the first great universities in recorded history. Some buildings were constructed by the Mauryan emperor Ashoka the Great (i.e. Raja Asoka: 273–232 BCE) which is an indication of an early establishment of the Buddhist learning center Nalanda. TheGupta Empire also patronized some monasteries. According to historians, Nalanda flourished between the reign of the Gupta king Śakrāditya (also known as Kumāragupta, reigned 415-55) and 1197 CE, supported by patronage from Buddhist emperors like Harsha as well as later emperors from the Pala Empire. The complex was built with red bricks and its ruins occupy an area of 14 hectares. At its peak, the university attracted scholars and students from as far away as China, Greece, and Persia. Nalanda was sacked by Turkic Muslim invaders underBakhtiyar Khalji in 1193, a milestone in the decline of Buddhism in India. The great library of Nalanda University was so vast that it is reported to have burned for three months after the Mughals set fire to it, sacked and destroyed the monasteries, and drove the monks from the site. In 2006, Singapore, India, Japan, China and other nations, announced a proposed plan to restore and revive the ancient site as Nalanda International University. </p>
    <p align="left">Majuli, in the state of Assam is the biggest river island of the world, situated on River Bramhaputra. The total area of this island district, which is also the first of its kind in the country, was once spread in an area of 1256 square kilometer, but now it has reduced to only 875 square kilometers, as the result of soil erosion. It is one of the famous travel destinations of North East India, especially during the time of the Majuli Bihu, a traditional dance festival. This island has been the cultural capital of Assam from as early as the 16th century, and is the home of people from the Missing tribe who had migrated from Arunachal Pradesh. Therefore, the locals can be seen speak Missing and Assamese language mainly.
    </p>
    <p align="left">
    Nalanda University was one of the first universities in the world, founded in the 5th Century BC, and reported to have been visited by the Buddha during his lifetime. At its peak, in the 7th century AD, Nalanda held some 10,000 students and 2000 teachers when it was visited by the Chinese scholar Xuanzang.
    </p>
    <p align="left">
    As historian Sukumar Dutt describes it, the history of Nalanda university “falls into two main divisions–first, one of growth, development and fruition from the sixth century to the ninth, when it was dominated by the liberal cultural traditions inherited from the Gupta age; the second, one of gradual decline and final dissolution from the ninth century to the thirteen–a period when the tantric developments of Buddhism became most pronounced in eastern India.Nalanda was one of the world’s first residential universities, i.e., it had dormitories for students. It is also one of the most famous universities. In its heyday it accommodated over 10,000 students and 2,000 teachers. The university was considered an architectural masterpiece, and was marked by a lofty wall and one gate. Nalanda had eight separate compounds and ten temples, along with many other meditation halls and classrooms. On the grounds were lakes and parks. The library was located in a nine storied building where meticulous copies of texts were produced. The subjects taught at Nalanda University covered every field of learning, and it attracted pupils and scholars from Korea, Japan, China, Tibet, Indonesia, Persia and Turkey. During the period of Harsha the monastery is reported to have owned 200 villages given as grants.
    </p>
