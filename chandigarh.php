    <h4 align="center">COMPLEX DU CAPITOLE</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Chandigarh_Capitol_Complex" target="_blank">
    <img src="capitol.jpg" align="right" alt=""></a>
</p>
    <p align="left">
    For Le Corbusier, Chandigarh’s Sector 1 is where the master architect unleashed his creativity, setting up the city’s most awe-inspiring landmark – The Capitol Complex.Both geographically and symbolically, the Capitol Complex occupies the compositional apex of the Chandigarh Plan. Located at the foot of the Shivalik Hills, together with the “Rajendra Park” stretching away on its western flank and Lake Sukhna to the east, it forms the Capitol Park, extending across the width of the city. </p>
    <p align="left"> When Punjab was rent asunder by the British, the Radcliffe Line awarded Lahore to western Punjab, leaving the eastern part of the (Indian) state without a capital.
    </p>
    <p align="left">
    The Capitol Complex stands out visually and physically from the rest of the city by a formal conception that embodies the dignity of the three fundamental powers – executive, legislative and judicial – of the two states of Punjab and Haryana, and of the Chandigarh administration.It is the largest monumental complex designed and built by Le Corbusier. He designed the three main buildings that make it up – the Palace of the Assembly, the Secretariat and the High Court of Justice – but also the furniture, the luminaries, the bas-reliefs moulded in the concrete, as well as works of art like the enamelled door of the Palace of the Assembly and a series of monumental tapestries. These three buildings are located on a pedestrian esplanade punctuated by other posthumous Corbusean creations: the Monument of the Open Hand, the Depth of Consideration, the Martyrs’ Memorial, the Tower of Shadows, the Geometrical Hill, as well as the reflecting pools of the High Court and the Assembly.
    </p>
    <p align="left">
    The Capitol mixes primary forms taken from Purist geometry with Brutalist expression punctuated by a polychromy of bold colours contrasting with the monochrome appearance of the raw concrete. The buildings combine European modernity – the many expressive variations of reinforced concrete – with local techniques and materials. The textures vary according to the various kinds of formwork and surface treatments. In order to preserve the authenticity of the materials, the outer surfaces were not coated but simply painted in places using Le Corbusier’s particular colour range.The composition of the site results from the desire to integrate the architecture into the landscape and the desire to reconcile man, nature and the cosmos. The human scale is connected to the monumental scale of the architecture and the public space by creating artificial earth mounds or tree and shrub plantations, and the glimpses of distant landscapes have been preserved.
    </p>
</article>
