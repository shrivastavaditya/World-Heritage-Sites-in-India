    <h4 align="center">ROSS Island</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Ross_Island,_South_Andaman_district" target="_blank">
    <img src="ross-island1.jpg" align="right" alt=""></a>
</p>
    <p align="left">
    Ross Island is a beautiful destination to visit when in Andaman and Nicobar Islands. This is a lovely place to tour if you want to make a day out it by traversing for hours amidst its historical importance. Ross Island was the headquarters of the British colony and today it houses its ruins of the buildings that were built during the British time. The Islands century old trees have captured few important landmarks by its wild vines, roots, and trunks like the Officers Quarters and rundown Church of the British times. Besides, some must visit attractions in Ross Island are like the Chief Commissioner's house, the Cathedral, and Graveyard of the Britishers. Today, if you visit the Island you will see the state of the art light and sound show commissioned by the tourism department to keep alive the vibes of the past. However, when in Ross Island, you are bound to get a great panoramic view of the nearby Island and get a chance to see a number of animals like peacocks, deers and ducks amidst the cool breeze and the amazing view of the sea. </p>
    <p align="left">Getting to Ross Island is not a difficult task, there are plenty of ferry ride from Marine Park, Port Blair. To cover the entire Island in a day, it is wise to take a ferry in the morning and return back in the afternoon because the ferries run from 7 am to 3 pm.Ross Island has become a hot spot tourist destination to see its ruins and savour the glory of the bygone days.
    </p>
    <p align="left">
    The best time to visit the Ross Island is during the months of November to February. These months are an ideal time to visit and explore the Island because during this season the weather remains fresh resulting best for sightseeing and other water activities.
    </p>
