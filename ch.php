<article>
    <h4 align="center">CHAMPARAN</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Champaran,_Chhattisgarh" target="_blank">
    <img src="chm.jpg" width=350px align="right" alt=""></a>
</p>
    <p align="left">
    Champaran was previously known as Champajhar. This village is of religious as well as historical significance as it is regarded to be the birthplace of the Sant Mahaprabhu Vallabhacharya, the founder, pioneer and reformer of the Vallabh Sect. It is thus, a popular Vaishnava Peeth. </p>
    <p align="left">
    Champaran is home to two temples that are dedicated to Shri Mahaprabhuji - the Prakatya Baithakji Mandir and Mool Prakatya (more commonly known as Chatti Baithak) and also holds an annual festival.
    </p>
   
