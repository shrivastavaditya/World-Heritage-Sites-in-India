<article>
    <h4 align="center">BOMDILA</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Bomdila" target="_blank">
    <img src="bom.jpg" align="right" alt=""></a>
</p>
    <p align="left">
    A very small hill station cuddled in the lap of Himalayas in West Kameng district of Arunachal Pradesh, Bomdila is no less than a slice of paradise on earth bestowed with a pious aura of spirituality, peace and solace. Sprinkled with frosty Himalayan peaks, virgin forests and gorgeous fruit orchards, the enchanting place, within no time, takes the visitors in complete awe of its breath-taking natural splendour. While undoubtedly, the quaint town is a soul-seekers’ retreat leaving them with an inexplicable sense of rejuvenation and transformation, it’s also a heaven for adventure lovers offering them with a wide array of outdoor adventure activities such as hiking, trekking, mountain biking and forest camping </p>
    <p align="left">Perched at an altitude of 3500 metres in Eastern Himalayan Range, Bomdila sits as the administrative headquarters of the West Kameng district of North East Indian state of Arunachal Pradesh. According to the historical records, the town was once a significant part of the Kingdom of Tibet and was reigned over by the local tribal rulers. It was during the time of British rule that Bomdila was captured by the British troops and made part of the Indian Territory. Even till date, though the town is considered an inseparable part of the Indian subcontinent, it still remains as a matter of dispute between India and China with the latter invading it in 1962 to reinforce its territorial claim.
    </p>
    <p align="left">
    While certainly, this quaint little town’s close proximity to the Indo-Chinese border makes it a sensitive zone with Indian Army troops deployed all around, the fact does not deter innumerable holiday makers, adventure enthusiasts and spirituality buffs coming here and spending a wonderful time in the lap of Mother Nature. The verdant forests, striking fruit orchards and numerous scenic trails in the region provide for an exhilarating time to all the nature lovers and adventure buffs; when here, venture on for some leisurely walks around the hill pathways revelling in entrancing nature’s vistas, go picking fresh and juicy apples right from the fruit farms or just sit back in hotel balconies and soak in the magic of setting sun painting the horizon in its pinkish-orangish hues; needless to say, each moment spent in Bomdila would be an experience one of its kind and off-course, a memory to treasure forever.
    </p>
    <p align="left">
    Highly influenced by Buddhist and Tibetan culture, Bomdila also makes as a great destination for avid anthropologists and sociologists too who are always in quest of some unique cultural experiences. Truly, a scenic wonderland tucked away in far-off corners of North East India, a trip to Bomdila is never short of spectacular and is sure to leave everyone with some unforgettable memories to be cherished through their lives.
    </p>
</article>
