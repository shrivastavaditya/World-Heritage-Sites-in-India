    <h4 align="center">LOTUS TEMPLE</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Lotus_Temple" target="_blank">
    <img src="LotusDelhi.jpg" height="300px" align="right" alt=""></a>
</p>
    <p align="left">
    Notable for a flowerlike shape and being one of the prominent tourist attractions in New Delhi, the Lotus Temple is located amidst the lush green landscape. Visiting the temple one could sense the pleasant and tranquil ambience which is perfect for the ones looking for meditation, peace and wisdom. The beautiful structure is visited by the locals and international tourists all throughout the year. The vicinity of the temple comprises gardens decorated with towering trees, short bushes and colorful flowers.</p>
    <p align="left">
        <b>ARCHITECTURE</b>
        <br>
        The construction of the splendid Lotus Temple took nearly 10 years before it finally got the shape of a lotus. Entering the temple complex tourists can witness that the building is composed of white marbles, cement, dolomite and sand with around nine crystal clear pools dotted over it. Also, tourists can witness the 27 freestanding petals that are made of marble. Also, there are nine reflecting pools that encompass the temple from outside that appears to be even more beautiful during the dusk. The temple has the capacity to accommodate nearly 2500 people and has nine doors that open in a central hall.
    </p>
    <p align="left">
    The Lotus Temple is open from Tuesday to Sunday between 9:30 AM to 5:00 PM during the winter season and 9:30 AM to 7:00 PM during the summer season. The temple is closed on Monday.Although the Lotus Temple can be visited all throughout the year, considering the winter season between November and February would be an ideal choice as the weather remains pleasant and favourable to explore the complex with less humidity.Shri Jagannath Marg, Tyagraj Nagar, Tyagraj Stadium, Near Safdarjung Bus Terminal, New Delhi - 110003.
