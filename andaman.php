    <h4 align="center">LONG Island</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Long_Island_(Andaman_and_Nicobar_Islands)" target="_blank">
    <img src="long-island1.jpg" align="right" alt=""></a>
</p>
    <p align="left">
    Long Island is said to be a modern village that has its own power-house, a boat building yard, senior secondary school, a bank, wireless facilities, hospital, Range Forest Office and even police outpost. In other words it is an ideal island settlement one can wish for. Long Island boasts beautiful beaches, cosy caves, green rolling hills and stunning mangroves. It also flaunts its lush green tropical forests including Tidal Swamp forests and Evergreen Forests, and a unique marine ecosystem. The top place to visit in Long Island is the silvery beach of Lalaji Bay. It is a gorgeous sandy beach on the west coast of Long Island and a boat cruise through the Mangrove creek here offers one of its kind experience.Apart from Lalaji Bay Beach, Merk Bay Beach is another beautiful place to see in Long Island. In fact, Merk is considered to one of the best beaches in Andaman. It is adorned with white sand in contrast with the blue water; the beach is safe for bathing and snorkeling as well as bird watching and sighting dolphins. Another worth seeing place in Long Island is Guitar Island. As the name suggests, the island is in the shape of a guitar. This lovely isle is ideal for relaxation and sunbathing. </p>
    <p align="left"> Long Island is situated about 82 kms from Port Blair and 35 kms north of Havelock. There are regular boats from Port Blair to Long Island thrice a week. There are buses from from Port Blair to arrive in Rangat, from where one can catch a 4pm boat from Yeratta (near Rangat) to Long Island.
    </p>
    <p align="left">
    Apart from basking in the sun, visitors can trek, snorkel and scuba dive. Visiting the Guitar Island is also a good idea. Also a boat ride to Merk and Lalaji Bay Beaches and shopping in Rangat Bazaar are some of the best things to do here.The ideal time to visit Long Island is between October and March, when the weather remains pleasant and free of humidity.
    </p>
</article>
