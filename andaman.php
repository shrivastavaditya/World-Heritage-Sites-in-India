    <h4 align="center">BARATANG Island</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Baratang_Island" target="_blank">
    <img src="baratang-island1.jpg" align="right" alt=""></a>
</p>
    <p align="left">
    Baratang Island is located about 100 km from Port Blair and if you take it from the tourism point of view, then this island is an unexplored gem filled with pure beauty and natural wonders. This must visit destination slouches with verdant green meadows and thick mangrove forests. Incredible yet overlooked place Baratang Island houses some unique and fascinating attractions like the impressive limestone caves and interesting mud volcanoes. The limestone caves in Baratang is a sedimentary rock formed and compressed over millions of years by the continuous deposits of many components and elements such as seashells, marine life, corals, and skeletons. Soon after you enter the caves, you are bound to start rubbernecking as you will find limestone formations hanging from the top and bottom. Besides, these caves are regularly emerging and growing in shapes and sizes. Another such attraction in Baratang Island that is visited by many tourists is the Mud Volcano. It so happens that the mud is pushed upward by a natural gas emitting from underneath created by decaying organic matter from underground. In addition to that, Parrot Island is also a must visit attraction located near Baratang Jetty. This beautiful place is best visited in the evening to witness thousands of parrots that fly around and roost. Baratang Island can be considered as a one-day trip destination but if you happen to dwell in its peaceful surroundings, then the Island provides accommodations like Forest and PWD guest houses. You can also avail a private accommodation in Baratang Island. </p>
    <p align="left"> Baratang is about 100 km away from Port Blair and can be reached via the grand Andaman truck road further you will be crossing the creek on a vehicle ferry. To reach the Island, government buses are available from Mohanpura Bus Terminus and private buses and taxis are also available where you will be picked directly from the selected areas or from Veer Savarkar International Airport.From Baratang Island you can take a ferry ride to reach limestone caves through a wide creek which leads to Nayadera Jetty. From there on you can walk to the attraction or stop at Broad-walk then walk to the attraction crossing Nayadera Jetty. Mud Volcano is easily reached via road to Nilambur jetty. From there on one has to get to Jarawa creek by ferry. From Jarawa Creek Mud volcano is walk able at about 300 meters. To reach the Parrot Island, one has to take a fibre boat from Baratang Jetty and cross a creek.
    </p>
    <p align="left">
    Expect to be surrounded by dense mangroves, jetty rides, soothing beaches, exotic birds, and cluster-phobic caves. Baratang Island hosts the tourists with many things to do. You can explore the Limestone Caves, sightsee parrots in Parrot Island and Inspect the Mud Volcano.November to February is considered the best months to visit Baratang Island. During this season the sea remains passable and fit for water activities, trekking and sightseeing.
    </p>
</article>
