<article>
    <h4 align="center">TIRUPATI</h4>
    <a href="https://en.wikipedia.org/wiki/Tirupati" target="_blank">
    <img src="tirup.jpg" align="right" alt="">
    </a>
    <p align="left">
    Situated in the Chittoor district of Andhra Pradesh, Tirupati is known for Lord Venkateshwara Temple, one of the most visited pilgrimage centres in the country. Tirumala is one of the seven hills in Tirupati, where the main temple is located. The temple is believed to be placed where Lord Venkateshwara took the form of an idol and is hence home to the diety Govinda. Tirupati is one of the oldest cities of India and finds mention in plenty of ancient Vedas and Puranas.</p>
    <p align="left">The non-stop chanting of ‘Om Namo Venkatesaya’, the mad pilgrim rush and the 8-feet tall idol of Lord Venkateshwara – everything about the Sri Venkateshwara Temple is majestic. Spread over an area of 26 kilometres and visited by nearly 50,000 pilgrims every day, the temple is also commonly referred to as the Temple of Seven Hills.
    </p>
    <p align="left">
    There are other temples in Tirupati too that you can visit, including the Sri Kalahasti temple, Sri Govindarajaswami Temple, the Kondandarama Temple, the Parashurameshwara Temple, and the ISKCON temple. Tirupati is home to a unique geological wonder that you shouldn’t miss out on! The Silathoranam is a natural arch formed out of rocks and is located at the Tirumala Hills.
    </p>
</article>
