    <h4 align="center">QUTUB MINAR</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Qutb_Minar" target="_blank">
    <img src="qutub.jpg" align="right" alt=""></a>
</p>
    <p align="left">
    Built in the 13th century Qutub Minar is not only the highest brick minaret in the world but also one of the famous historical landmarks of India. Being the cultural UNESCO World Heritage Site, Qutub Minar attracts thousands of visitors everyday. Visiting the tourist spot, one would come across the surrounding archaeological area that comprises funerary buildings, notably the magnificent Alai-Darwaza Gate, the masterpiece of Indo-Muslim art which was built in the year 1311, and two mosques.Witnessing the monument, you will be surely enthralled by the huge structure that withstood all tests of time. Rising up to 72.5, Qutub Minar have 379 steps which is required to be covered to reach to the top. Built out of redstone and marble, the three stories from the base are built using red sandstone while the top two stories are made of marble and sandstone. </p>
    <p align="left"> The magnificent architectural structure was built to propound the prominence of the Islam religion. Also, it was not completely built in one go rather it underwent various repairing works for the restoration of this structure by maintaining the Indo-Islamic Style inspired from the Minaret of Jam in Afghanistan. Entering the minaret, one would come across the Arabic inscriptions on the walls of the tower that truly depict the historical importance of the structure.
    </p>
    <p align="left">
    Being the popular tourist spot in Delhi, the monument remains open from sunrise to sunset for the locals as well as both national and international tourists. So, don’t miss out visiting the imposing structure Qutub Minar during your visit to Delhi to make the most out of your tour in India.Qutub Minar can be visited in all seasons all through the year. To experience and admire the beauty of Qutub Minar, try visiting during the winter season when the weather remains cool.
    </p>
    <p align="left">
        <b>HISTORY</b>
        <br>
        The construction of Qutub Minar was started in the year 1199 AD by Qutb ud-Din Aibak, the founder of the Mamluk Dynasty in Delhi which was later completed by his successor and son-in-law Iltutmish. Although the origin of the Qutub Minar is shrouded with controversy and some believe it was erected as a tower of victory to signify the beginning of Muslim Rule in India. While, some believe it was constructed to call the faithful to prayer.
    </p>
