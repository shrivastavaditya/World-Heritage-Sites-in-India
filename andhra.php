<article>
    <h4 align="center">AMARAVATI</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Amaravati" target="_blank">
    <img src="amra.jpg" align="right" alt=""></a>
</p>
    <p align="left">
    Known as the Capital of Andhra Pradesh, Amaravathi is a planned city on the banks of the River Krishna. Amaravathi is renowned for being a site of a Buddhist Stupa that is a semi-hemispherical structure containing Buddhist relics and often called the Abode of God. </p>
    <p align="left">Located 32 kilometres north of Guntur District, Amaravati is a prime hub of pilgrimage and sightseeing. The original structure was established during the reign of Emperor Ashoka which is now a meditation site in the city.
    </p>
    <p align="left">
    Being built on a 217km riverfront, the city is designed to have 51% of green spaces. Its foundation stone was laid on 22 October 2015 by prime Minister Narendra Modi. After the separation into two states in the year 2014, Hyderabad was declared the capital city of the then newly formed state Telangana.
    </p>
    <p align="left">
    It led to Amaravathi being chosen as the Capital of Andhra Pradesh. The word Amaravathi in itself was derived from the ancient capital of Satavahana dynasty. Pilgrims visit this town almost throughout the year owing to the number of temples and shrines that adorn the little town in different parts.
    </p>
</article>
