    <h4 align="center">PORT BLAIR Island</h4>
    <a href="https://en.wikipedia.org/wiki/Port_Blair" target="_blank">
    <img src="andamanpic (2).jpg" align="right" alt="">
    </a>
    <p align="left">
    Port Blair is a popular tourist destination and it is the capital city of the Andaman and Nicobar Islands. The Island is surrounded by rugged coastline and tropical forest but despite the fact for having hot and moist biome, this Island has a picturesque environment. Port Blair blooms with various tourists flying from different places and it is evident that this place sees different faces every year. The city is popular for the infamous Cellular Jail National Memorial, which was once a prison where many freedom fighters and people of other nationals were imprisoned. Today, this place serves as a famous attraction that must be visited. Port Blair attracts tourists from all over the country not just for being a clean city but for its remarkably beautiful coral reefs. </p>
    <p align="left"> Tourism in Andaman and Nicobar will take you to breathtaking interesting places and to some major tourist attractions like Ross Island, earlier this island was the seat of British administration. Viper Island, this island was a site to imprison the convicts before the Cellular Jail was constructed. Radhanagar Beach, this is a beautiful beach voted to be the best beach in Asia by the TIME 2004. Cellular Jail, also known as Kala Pani was used by the British to imprison political prisoners, today; this site serves as a museum. Havelock Island, this island is the largest of all the islands in Andaman. Port Blair has several appealing beaches that are serene and beautiful including various beach activities like parasailing, banana boat ride, as well as underwater and water activities like scuba diving, sea walking etc. The capital offers tourists with a fulfilling holiday experience; be it with families, couples, honeymooners or adventure enthusiasts. Port Blair, an ultimate holiday destination has something for everybody.
    </p>
