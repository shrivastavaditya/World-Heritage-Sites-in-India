<article>
    <h4 align="center">SIVASAGAR</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Sivasagar" target="_blank">
    <img src="siv.jpg" align="right" alt=""></a>
</p>
    <p align="left">
    One of the holistic towns, Sivasagar formally called as Sibsagar is located in the upper ranges of Assam. This popular destination in Assam is set amidst the rich and diverse biodiversity surrounded by many historic attractions. Tourism in Sivasagar is popular for its Ahom monuments and palaces which are a work of excellence. And circled by the lovely ambience and graceful hills this place has a number of other attractions as well. Hence, it is a perfect destination to fascinate and engage in the lovely attractions on Sivasagar.</p>
    <p align="left">With booming tea and oil industries, tourism is an additional bonus point for the town of Sivasagar. The literal translation of Sivasagar stands for “Ocean of Shiva”. This travel destination in Assam is popular for its Ahom monuments and palaces which are quite exquisite. One of the top things to do in Sivasagar is to visit Sivasagar Sivadol Temple which gave this town its name and was built during the year 1734 by Kuwori Ambika, the wife of Swargadeo Siba Singha.
    </p>
    <p align="left">
    If planning your holidays around the time of Shivratri, you have the opportunity to witness and enjoy the grand celebration by the devotees of Lord Shiva. You can also visit Devidol which is dedicated to Devi Durga and hosts Durga Puja during the months of April-May and September-October; Vishnudol that is dedicated to Lord Vishnu; and Ghanashyam Dol, which is a marvellous temple built from terracotta and a part of its original path way still exists. Amongst palaces, you can visit the Talatal Ghar that was originally built to be an army base and comprises of several hidden passageways constructed underneath till the Rangpur Palace.
    </p>
    <p align="left">
    One of the sightseeing places of Sivasagar is the Sivasagar Tai Museum which houses several artefacts from Ahom Dynasty and its rulers. There are also several tanks which you can visit during your tour in Sivasagar like Joysagar, which is reckoned to be the largest artificial lake ever made in an area of 1.29 km2; and Rudrasagar and Gaurisagar, out of which Rudrasagar is more beautiful one and has been built around a Shiva temple.
    </p>
</article>
