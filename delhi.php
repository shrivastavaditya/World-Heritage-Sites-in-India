    <h4 align="center">AKSHARDHAM TEMPLE</h4>
    <a href="https://en.wikipedia.org/wiki/Swaminarayan_Akshardham_(Delhi)" target="_blank">
    <img src="akshar.jpg" align="right" alt="">
    </a>
    <p align="left">
    Located in the heart of India i.e., New Delhi, Akshardham Temple is a marvel of architecture that unfolds the cultural legacy of thousands of years and is a tribute to Lord Swaminarayan (1781- 1830) who is the avatars, devas and great sages of Hinduism. The construction of the grand structure took almost 5 years and was inaugurated on 6 November, 2005. Today the imposing structure is standing on the banks of the Yamuna River and attracts millions of tourists and pilgrims across the globe. </p>
    <p align="left">Basically, the term Akshardham has been derived from two words 'Akshar' which means eternal and 'Dham' which means abode, which together means abode of divine or the eternal. Entering the temple, one could see the 11 feet high gilded image of Bhagwan Swaminarayan that appears mesmerising. Visitors could feel each element of Akshardham echoing with spirituality that makes every soul potentially divine.Witnessing the main temple, the other exhibitions takes the visitor to the additional sighting that includes the Yogi Hraday Kamal, which is a garden in the shape of lotus, the Nilkanth Abhishek, a place to offer abhishek which is a ritual of pouring water in form of respect and prayers, the Narayan Sarovar which is a lake containing water from 151 different rivers that rims the temple, Premvati Ahargruh (restaurant which caters traditional vegetarian food) and more.Travellers coming to see Akshardham Temple get fascinated by decoration of the temple which comprises minute carved deities, flower, fauna and musicians. The temple consists of 234 pillars, 9 domes, and twenty thousand statues of acharyas, sadhus, and devotees. A visit to Akshardham Temple is a spirituality enriching experience. It is the beauty and magnificence of Akshardham Temple that makes it one of the must visit tourist attractions in Delhi.
    </p>
    <p>
        <b>History</b>
        <br>
        The construction of Akshardham Temple was brainstormed by Yogiji Maharaj which was created by Pramukh Swami Maharaj, the former leader of BAPS (Bochasanwasi Akshar Purushottam Sanstha). Initially, the place for the construction wasn’t decided and different places including Ghaziabad, Gurgaon, and Faridabad were suggested to the Guru but he stood firm to the dream of Yogiji Maharaj who wanted to get it constructed on the banks of the Yamuna River. So keeping this in mind, Pramukh Swami Maharaj finally acquired an area of 30 acres on the banks of Yamuna and started with the construction process. The temple was consecrated on 6 November 2005 and today Akshardham Temple attracts around 70 percent of all the tourists coming from across the globe to Delhi.
    </p>
    <p>
        <br>
        To visit Akshardham Temple the convenient and best way is to travel by Delhi Metro. The nearest metro station to Akshardham Temple is Akshardham Metro station which is located on the Blue Line of Delhi Metro.A visit to Akshardham Temple can be made all throughout the year. But winter season between November and February is considered to the best time as the weather remain cool and pleasant.
    </p>
