    Positioned near the Port Blair, Viper Island was the location of old jail used by the British to keep political prisoners and harbour convicts. This island is exceptionally exquisite and tranquil. The name of this island is kept after the name of the vessel in which Lt. Archibald Blair came to Andaman and Nicobar in the year 1789. This beautiful island is swarmed by visitors as it serves a wonderful amalgamation of historically significant elements and arresting grandeur of nature. Thus, Viper Island is no less than an empyrean for both the bugs of history and nature admirers.</p>
    <p align="left">Ferries are available between Viper Island and Port Blair.One can take a tour of the old jail building. During the boat journey to this island one can also see the main port and navy ships of Port Blair. The panoramic view of seven points from the harbour is also something to relish here.
    </p>
    <p align="left">
    Though one can visit the island throughout the year, the ideal time would be between November and February when the weather cools down a bit and the humidity is almost zero.
