    <h4 align="center">MUSEUM OF EVOLUTION OF LIFE</h4>
    <p align="right">
        <a href="https://www.nativeplanet.com/chandigarh/attractions/museum-of-evolution-of-life/" target="_blank">
    <img src="dead.jpg" align="right" width=30% alt=""></a>
</p>
    <p align="left">
    Museum of Evolution of Life is located in sector 10 of Chandigarh. This museum is amazing as it signify the history of India from the time of Indus valley civilization till to know. This museum was started on 13 August 1973. As its name indicates it represent the growth of life from the unicellular creature to the natural variety. Museum of Evolution of Life is best place for those who are concerned in evolutionary learning. This museum has special art arcade in the fields of archaeology, astronomy, botany, biology, geology and history. There is a big allusion library, where you can find a plenty of useful books. There are a variety of paintings that represent the derivation of existence and also there are diversity and variety of plants and animals, vanished battle of man and etc. </p>
    <p align="left"> The Museum of Evolution of Life, as the name suggests, traces back the origin of a man from Indus Valley Civilisation to the present age. Set up in 1973 as a part of the Government Museum and Art Gallery, this exquisite museum houses portraits and paintings that explain the evolution of humans from unicellular organisms to biological diversity. The first phase of the museum was the section of Cyclorama that was followed by Astronomy, Archaeology, Pre-historic and Geology sections. There is also a reference library inside the museum that holds an outstanding collection of books on subjects like evolutionary studies, palaeontology, zoology, botany and biology. The museum is open from 10 am to 4:30 pm throughout the week, except on Mondays.
    </p>
