<article>
    <h4 align="center">SIRPUR</h4>
    <a href="https://en.wikipedia.org/wiki/Sirpur,_Mahasamund" target="_blank">
    <img src="sirp.jpg" width=450px align="right" alt="">
    </a>
    <p align="left">
    Sirpur is a small village located in the state of Chhattisgarh, situated on the banks of river Mahanadi. It is 35 km away from the Mahasamund district and around 78 km away from the city of Raipur, which is the capital of Chhattisgarh. The village of Sirpur is an archaeological wonder. Inspiration to many architects, this village is rich in its temple culture. A quaint hidden gem, it has a deep connection to the Buddhism world and a treasure for archaeological findings from the 8th century.</p>
    <p align="left">There are numerous temples one can visit, and is generally a hub for passionate historians. A look at the historical artefacts and the deep carvings on the walls of the temples have inspired many architects around the world. The Buddhist monasteries of this village are said to be one of the most important and most significant in India. Apart from the rich historical importance and fascinating findings in these villages, there is more to this village. The Chhattisgarh Tourism Board organises a music and dance festival here to promote the Buddhist sites and celebrate its culture. Offering a rare mix of art and culture with different performance and a deep history and evolution of art, Sirpur is a peaceful village filled with wonders.
    </p>
</article>
