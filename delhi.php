    <h4 align="center">RASHTRAPATI BHAWAN</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Rashtrapati_Bhavan#:~:text=The%20Rashtrapati%20Bhavan%20(%20pronunciation%20(help,Hill%2C%20New%20Delhi%2C%20India." target="_blank">
    <img src="rb.jpg" align="right" width=100% alt=""></a>
</p>
    <p align="left">
    Etched to the heart of Central Delhi from the year 1929 is the most wonderful landmark in the history of Indian architecture, Rashtrapati Bhavan. Also known by the name of Presidential Residence and Viceroy’s House, this tourist attraction is the official home of the President. Looking back in history we get to know that the Rashtrapati Bhavan was sculpted as the residence of British Viceroy when the decision of making Delhi the new capital was made firm. It not only took 17 years to complete this marvel, but the rigorous efforts of Edwin Landseer Lutyens, the British architect who turned his sketches and thoughts into what he actually sent to Herbert Baker in the year 1912. 26 January 1950 was the time when the residence was renamed as Rashtrapati Bhawan under the rule of the first President of India, Rajendra Prasad. </p>
    <p align="left"> What we see now is a massive architectural marvel which was built during the time when Edwardian Baroque style of architecture was prominent. The style emphasised on showcasing heavy classical motifs which symbolised imperial authority and legacy. Spread over an area of 321 acre, this tourist attraction has 340 rooms, huge open space, Mughal Gardens to admire, reception halls, guest rooms, stables, offices, residence for staff and bodyguards, and much more. All of which bestows upon it the honour of being one of the world’s largest residences of a head.
    </p>
    <p align="left">
    President house is open to public from 9:30 AM to 5:30 PM on Monday to Friday. It is closed on Saturday and Sunday.Central Secretariat serves as the nearest metro station to Rashtrapati Bhavan settled some 700m away. The metro station falls on the Yellow Line and is also linked to the Violet Line of Delhi Metro.
    </p>
    <p align="left">
        <b>HISTORY</b>
        <br>
        The whole idea of constructing Rashtrapati Bhavan started taking shape in the year 1911 when it was decided that Delhi would be crowned as the new capital. This decision led to the thought of raising a residence for the British Viceroy which would stretch up to 4,000 acres along with the Secretariat Building. To lay the foundation the Malcha and Raisina villages which served as the shelter to over 300 families were relocated under the Land & Acquisition Act. The famous British architect of that time, Edwin Landseer Lutyens was handed over the responsibility to carve out the beautiful residence. Much to everyone’s surprise, the Presidential Residence was somewhat similar to what Lutyens had sketched in Simla in the year 1912 and sent to Herbert Baker. The architectural style adopted for the construction of Rashtrapati Bhawan was inspired by Edwardian Baroque architecture and Indian architecture. While Baker began with constructing the Secretariat Buildings, Lutyens had it in his head to make the residence on top of Raisina Hill which however didn’t come out as he thought it to be. What we see today, was a feared model of Lutyens as he wanted the angle of the road to be steeper as it overshadows the front of the marvel.
    </p>
