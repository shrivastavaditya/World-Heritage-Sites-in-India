    <h4 align="center">INDIA GATE</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/India_Gate#:~:text=The%20India%20Gate%20(formerly%20known,Delhi%2C%20formerly%20called%20duty%20path." target="_blank">
    <img src="indiagate.jpg" align="right" alt=""></a>
</p>
    <p align="left">
    India gate or The All India War Memorial is 42-metre-high giant gate made up of sandstone, built by prominent war memorial architect Edwin Lutyens in 1921. It is a war memorial situated at the eastern end of Kingsway that is the Rajpath, is memorial to martyrs of World War (1914-21). During the liberation period of Bangladesh, a structure comprising of a black marble plinth along with reversed L1A1 rifle, capped by a war helmet surrounded by four urns igniting the permanent light was constructed under the India gate. This structure is called the Amar Jawan Jyoti. It was inaugurated during the 23rd Republic day by the then prime minister Indira Gandhi in 1972. Since then Amar jawan Jyoti has been serving as a gravestone dedicated to the known or unknown Indian martyrs, who lost their lives while defending the nation. The names of more than 13,000 Indian martyrs are inscribed on the monuments. </p>
    <p align="left">The present government is planning to build national war memorial near the India gate canopy which recently underwent the removal of George V statue which was later shifted to the coronation garden.
    </p>
    <p align="left">
    People visiting India Gate experiences true sense of patriotism. It is one of the best tourist attractions in New Delhi having widely spread children park and boating club. It serves as a best picnic spot. India gate is also famous for being the venue of several march and demonstrations.One can visit India Gate at any time of the day and night. Boating timings are limited from 2pm to 9pm. The light show starts from 7pm to 9pm.
    </p>
    <p align="left">
    The foundation of this war memorial was laid in the year 1921 in the month of February. The ceremony was attended by the Duke of Connaught, Delegates and Officers of the British Indian Army, Imperial Service Troops and the Viceroy. The construction of India Gate was a project initiated under the Imperial War Graves Commission, which was famous for building war tombstones of the era.
    </p>
