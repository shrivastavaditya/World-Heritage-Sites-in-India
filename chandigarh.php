    <h4 align="center">ROCK GARDEN</h4>
    <a href="https://en.wikipedia.org/wiki/Rock_Garden_of_Chandigarh" target="_blank">
    <img src="rock.jpg" align="right" alt="">
    </a>
    <p align="left">
    Chandigarh has the distinction of having a unique world-acclaimed Rock Garden. It consists of art objects, fashioned from industrial and urban waste. It is situated between the Capitol Complex and Rock Garden of Chandigarh Sukhna Lake in Sector 1. It nestles amidst 20 acres of woods in the form of an open air exhibition hall, theatre trove and a miniature maze all rolled into one vast fantasy land of art and landscape. It is without doubt, a tourist spot that is a must on the itinerary of every visitor to Chandigarh. </p>
    <p align="left">An unpretentious entrance leads to a magnificent, almost surrealist arrangement of rocks, boulders, broken chinaware, discarded fluorescent tubes, broken and cast away glass bangles, building waste, coal and clay-all juxtaposed to create a dream folk world of palaces, soldiers, monkeys, village life, women and temples. The open air sculptures and concealed gateways separating them are at places enhanced by a waterfall, pools and an open air theatre with proper stage setting. Several prestigious performances have been staged in this small but very artistic and naturalistic open air theatre.
    </p>
    <p>
        <b>History</b>
        <br>
        The Rock Garden has become almost a heritage site. Artists and connoisseurs from all over the world flock to see this unique and amazing creation. The concept is daring, the appeal perennial. Visitors leave in admiration, only to return again. The creator of the Rock Garden, Nek Chand, was a Road Inspector in the Engineering Department of Chandigarh Capital Project. He roamed the Shivalik Foothills and picked up stones resembling bird, animal, human and abstract forms. He brought them on his bicycle. The first seven years (1958-65) were spent collecting natural material, urban and industrial waste. Gradually his collection mounted to a staggering twenty thousand rock forms of amazing beauty. These were deposited around a hut which he had built for his work and contemplation. He built the Rock Garden from this improvised hut which was located by the side of a stream.On February 24, 1973, the Rock Garden was accidentally discovered by Dr. S K Sharma who headed an Anti-Malaria party during a vector reconnaissance duty in the forest in which the garden is located. Soon thereafter the late Dr. M S Randhawa ( a former Chief Commissioner of Chandigarh ) visited the place and placed the matter before the Chandigarh Landscape Advisory Committee, as its Chairman, at a meeting held on June 23, 1973, suggesting that this garden of rocks, stones and scrap was the most unusual and it should be preserved in its present form, free from the interference of architects and town planners. The Rock Garden was thus inaugurated in 1976.
    </p>
    <p>
        <b>Collection</b>
        <br>
        Since the site where Rock Garden stands today was also used as a dumping ground for urban and industrial waste, Nek Chand picked up pieces of foundry lime-kiln and metal workshop wastes and also shaped them with his creative genius, resembling human, animal or abstract forms. These pieces have been innovatively displayed as sculptures in the garden.Also, on display are shapes sculptured by using a variety of discarded materials such as frames, mudguards, forks, handle bars, metal wires, play marbles, crowns of soft drink bottles, porcelain, auto parts, broken bangles, pieces of slate, burnt bricks and even human hair recovered from barbers' shop.The layout of the Garden is based on the fantasy of a lost kingdom. The moment one enters the garden, the small entrance doors make the head bow, not only create an ambience of royal grandeur but also impart humbleness. One has to pass through a variety of doorways, archways, vestibules, streets and lanes of different scales and dimensions, each one opening into a new array of displays or courtyards and chambers lending an air of suspense and curiosity at every corner, at every turn !

In the true spirit of a make-believe `kingdom' the Rock Garden has fourteen different chambers, like the forecourt : housing natural rock-forms, a royal poet's and a musician's chamber complete with a pond and a hut; the main court (Durbar) where the king's throne adorns the place with natural stone forms depicting gods and goddesses lining the place; a swimming pool for the queen, etc. Another phase of the garden comprises the grand palace complex, minars, water falls, an open air theatre, a village, mountains, overbridges, pavilions and areas for royal pleasures. The tree and root sculpture offers a powerful counterpoint to the existing vegetation.An open air theatre and a vast pavilion with a centre stage are the other highlights of the Rock Garden where art and culture blend amidst the rustic and exotic environs of the garden.The Teej Festival, when the Rock Garden assumes a festive look, holds a special attraction for tourists. Young damsels partake in the fun and frolic by swaying on the giant swings, while others adorn their hands with traditional Mehandi (henna), amidst joyful songs and dances.As you stroll through the Rock Garden, enjoying the awe inspiring creation, you may find yourself face to face with the unassuming, down to earth artist Nek Chand himself, in flesh and blood, working at or supervising his 'kingdom'.
    </p>
