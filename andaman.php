    <h4 align="center">NEIL Island</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Neil_Island" target="_blank">
    <img src="neil-island1.jpg" align="right" alt=""></a>
</p>
    <p align="left">
    On the Northeast side of Port Blair, Neil Island is the Vegetable bowl of Andaman, owing to the farming practices that take place here. A long and the immaculate island of Andaman, Neil Island is endowed with exquisite settings, which makes it just right the holiday. For tourists in quest of chilling out after the humdrum schedule, this island is no less than an empyrean. The azure sea and verdant foliage make it treat to the eyes. There are six beautiful beaches in Andaman like Sitapur, Lakshmanpur-I & II, SIR HUGH ROSE ISLAND and Bharatpur - each with the best view of the sun gliding below, and above the horizon. Also, the beaches of Andaman are quite famous for adventure activities like snorkeling and scuba diving. If the idea of adventure activity doesn't sound exciting then do visit the beachside to soak in the cool breezy air, read a book and sip the natural coconut water. Do visit the spectacular corals and reefs in Neil Island. </p>
    <p align="left"> From the Phoenix Bay jetty at the Port Blair, government run private boats operate daily to Neil Island. On an average, it takes 55 minutes to reach Neil Island from Port Blair.Luxury, adventure, privacy, and beauty are all rolled up into the Neil Island. Apart from sightseeing, one can take part in adventure activities like Scuba diving in Jetty area and Laxmanpur beach-I; Swimming, and Snorkeling at beaches in Sitapur; Cycling; Glass Bottom Boating, and Game Fishing are some of the prominent things to do over a Neil Island tour.
    </p>
    <p align="left">
    Neil island experience a tropical climate; that is why the best time for a holiday here for sightseeing will be from October to May. During this time, the weather is is good enough to go for sightseeing and watersports activities. Try to plan a vacation to Neil Island around January so as to attend the Subhash Mela, celebrated to honor the birth anniversary of Netaji Subhash Chandra Bose. Also during January, the Island Tourism Festival takes place. Flower show, vegetable show, handicraft stalls, healthy baby contest, and food stalls are the major highlights of the event.
    </p>
