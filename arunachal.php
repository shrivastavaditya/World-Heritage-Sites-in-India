<article>
    <h4 align="center">BHALUKPONG</h4>
    <a href="https://en.wikipedia.org/wiki/Bhalukpong" target="_blank">
    <img src="bhalu.jpg" align="right" alt="">
    </a>
    <p align="left">
    A small town nestled in the foothills of Himalayas in West Kameng district of Arunachal Pradesh, Bhalukpong is known for its breath-taking scenic beauty and serene and tranquil ambience which casts a spell on its every visitor. Home to virgin forests, bubbling brooks and gurgling Kameng River, the place, besides providing for a soul-soothing retreat to all the distressed souls, also makes as an excellent adventure tourist destination offering a scope for plethora of outdoor activities like hiking, trekking, fishing, angling, camping and river rafting. The tiny little hamlet’s inhabitation of various ethnic tribes makes it an anthropologist’s paradise too giving them ample opportunities to have a sneak-peek into these native tribes’ fascinating cultures, lifestyle and traditions. Truly, one of the best kept secrets of the North East India, Bhalukpong is sure to leave anyone and everyone enchanted for their lifetime.</p>
    <p align="left">Situated on the banks of the beautiful Kameng River, Bhalukpong lies in close proximity to the Assam-Arunachal border, just 60 km north of Tezpur and makes as the most significant entry point for the Western Arunachal Pradesh circuit. Its unparalleled scenic charm and innumerable opportunities to revel in great outdoor fun truly makes this serene little hill town a hidden gem, away from the radars of conventional tourist maps.
    </p>
    <p align="left">
    Anyone heading for a trip to much famed destinations of Arunachal like Tawang and Bomdila, must plan to stop by here for a couple of days and soak themselves in immense tranquillity of the place, occasionally broken by light whispering of the wind, playful music of the effervescing streams and melodious chirps of colourful feathered birds; go for a leisurely stroll in the woods basking in nature’s creativity or plan for an enthralling forest camp amidst its serene wilderness gazing at thousands of stars sprinkling their luminescence in the dark sky, every moment spent in this little village would bring in a new experience and would be a memory to treasure forever.
    </p>
</article>
