    <h4 align="center">MALHAR</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Malhar,_Chhattisgarh" target="_blank">
    <img src="malh.jpg" width= 380px align="right" width="600px" alt=""></a>
</p>
    <p align="left">
    Malhar is Chhattisgarh’s most historic town and is of utmost archaeological prominence. Its outstanding sculptures and ancient charm has bagged a spot for this town on the ‘Ancient Monuments and Archaeological Sites and Remains of National Importance’ list that is maintained by the Archaeological Survey of India.
    </p>
<p align="left">
    Few of the remains found in Malhar date back to 1000 century BC and are attributed to the Kalchuri regime.
</p>
    
</article>
