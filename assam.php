    <h4 align="center">GUWAHATI</h4>
    <a href="https://en.wikipedia.org/wiki/Guwahati" target="_blank">
    <img src="guw.jpg" align="right" alt="">
    </a>
    <p align="left">
    The largest, and the most important city in the region, Guwahati is a gateway to the Northeast where all the buzz takes place. It is here where one can find the post hotels, international airport, commercial hubs, local flavors and range of temples to explore. Scattering, Isn't it? When on a holiday in Guwahati make a point to visit Kamakhya Mandir, Bhubaneshwari Temple, ISKCON Temple, and Vashistha Ashram. There are some handful cultural centers to visit like Srimanta Sankardeva Kalakshetra.</p>
    <p align="left">Guwahati located in the state of Assam serves as one of the most visited destinations in North East India. The former name of Guwahati was Pragjyotishpur which means the city of eastern lights. Situated on the banks of the Brahmaputra River and surrounded by the Shivalik Hills, Guwahati serves a perfect getaway. The city has a plethora of tourist attractions which makes holidays here even more enjoyable and fun.
    </p>
    <p align="left">
    Among the many well known sightseeing places, the Kamakhya Mandir is one of the prominent religious sites of the city. This temple is known as one of the Shakti peethas as per the Hindu mythology. Many devotees from great distances travel to this temple to offer their prayers to the goddess. The shrine is located at the top of the Nilachal Hill and can be easily reached by availing any local means of transportation from the city. Another notable temple in the city is the Bhubaneshwari Temple which is located just above the Kamakhya Mandir.
    </p>
</article>
