<article>
    <h4 align="center">MAJULI</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Majuli" target="_blank">
    <img src="maj.jpg" align="right" width="400px" alt=""></a>
</p>
    <p align="left">
    One-of-its-kind - Majuli amidst the mighty yet turbulent Brahmaputra happens to be the World’s largest riverine island. Make a point to visit their islands of Majuli, all of which happens to be the bird watchers paradise. Besides, it is known for its sataras, the Hindu Vaishnavite monasteries, and centers for art. Do visit the satras of Assam, especially the Dakhinpat Satra, Garamurh Satra, and Auniati Satra to see the well nourished Assamese dance forms, music, and dance drama. Majuli is located at a distance of around 200km east from Guwahati. And there are no hotels yet there are homestays. </p>
    <p align="left">Majuli, in the state of Assam is the biggest river island of the world, situated on River Bramhaputra. The total area of this island district, which is also the first of its kind in the country, was once spread in an area of 1256 square kilometer, but now it has reduced to only 875 square kilometers, as the result of soil erosion. It is one of the famous travel destinations of North East India, especially during the time of the Majuli Bihu, a traditional dance festival. This island has been the cultural capital of Assam from as early as the 16th century, and is the home of people from the Missing tribe who had migrated from Arunachal Pradesh. Therefore, the locals can be seen speak Missing and Assamese language mainly.
    </p>
    <p align="left">
    The island has been a home for Neo Vaishnavaite tradition for a long time. In Majuli, there are about 22 Vaishnava Satras which are well known attractions of the place. Among the Satras, the first one was built by Sankaradeva, who is reckoned to be the father of the Neo Vaishnavism. Some of the important Satras of the area are Dakshinpath, Garamur, Auniati and Kamlabari. These count as the must visit tourism places of Assam too.
    </p>
    <p align="left">
    Among the top things to do in this island includes taking a peak of the cultures and traditions of the tribes of the island; and the best time to do it would be during the Bihu festival as you can see soak in all the excitements of the fairs. Bird watching in the lush greenery is another top thing to do on the tour as well. Planning your holidays around the festival will let the visitors enjoy a number of adventure sports. There are other tourism places near the Majuli Island, which you would like to visit during your stay, like Jorhat, Kaziranga National Park, Dhemaji, Lilabari, Sibsagar etc.
    </p>
</article>
