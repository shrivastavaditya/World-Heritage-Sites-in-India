<article>
    <h4 align="center">DAPORIJO</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Daporijo" target="_blank">
    <img src="dapo.jpg" width="500px" align="right" alt=""></a>
</p>
    <p align="left">
    Nestled on the shores of the beautiful Subansiri River, Daporijo, blessed with an aesthetic natural splendour of bamboo rainforests, lush paddy fields and enchanting waterfalls makes for a great hideout for nature lovers, adventure enthusiasts and soul seekers alike. The little place falls enroute to Along from Ziro Valley and serves as home to the people of Tagin Tribe who are famed for their unique cultures, traditions and religious practices. In case one is looking forward to an exceptional offbeat cultural rendezvous during their holiday trip, would surely fall in love with this tiny hill hamlet that is often referred to as the hidden little jewel of North East India.</p>
    <p align="left">Daporijo is a small hill village that sits in the upper region of the Subansiri district in Arunachal Pradesh and makes for a perfect retreat for all those holiday seekers looking for an unconventional tourist destination where they can break away from the shackles of suffocating city lives. The picturesque beauty of Daporijo is something which cannot be summed up in just a few words; tall bamboo forests, pastoral paddy fields and pristine River Subansiri flowing just nearby lends it an alluring natural scenery which seems to have made its way directly out from a picture postcard.
    </p>
    <p align="left">
    While this scenic little town is truly a nature lover’s delight, it’s also a paradise for adventure seekers offering them ample opportunities to satiate their adrenaline desires venturing on activities like trekking, hiking and river rafting. Since the village has rich treasures of numerous ancient caves, it also makes as an idyllic place for avid cavers to embark on enthralling caving expeditions.
</p>
    <p align="left">
    Bamboo shoot is widely eaten all over the state and makes as the main ingredient for all the side dishes, be it fresh greens, meats, pickles or chutneys. Fish is found in abundance in Daporijo so, trying out Ngatok, an authentic fish curry absolutely makes for a must try when in the town. A meal in Daporijo is not complete without sipping on some Apong which is a light homemade beer made of variety of rice and an important asset of every household in Arunachal.
    </p>
</article>
