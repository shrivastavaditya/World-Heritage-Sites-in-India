    <h4 align="center">HAVELOCK Island</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Havelock_island" target="_blank">
    <img src="havelock-island1.jpg" align="right" alt=""></a>
</p>
    <p align="left">
    Among the islands of Andaman, the one that is most visited is the Havelock. Spread in an area of 113 sq. km. and located 39 km of north-east of Port Blair, Havelock Island offers a perfect beach vacation in Andaman. The white sand beaches of this congenial and imposing island are fringed with rain-fed forests making them quite a beautiful destinations to see in India. Famous Radhanagar Beach and Elephant Beach are also part of Havelock Island along with Kalapather and Vijaynagar Beach. </p>
    <p align="left"> In addition to the extraordinary ambiance and hypnotizing tranquility, Havelock Island also proffers enthralling adventure activities such as scuba diving, snorkelling and trekking. Equipped with several recreational and other pleasant services for tourists, Havelock Island is a supreme destination for a vacation in nature's lap.
    </p>
    <p align="left">
    Government ferries connect Phoenix Bay jetty, Port Blair to Havelock Island. Private luxury boats also ferry tourists to Havelock daily. At Havelock auto rickshaws, taxis, buses, motorbikes are available for local transport. The STS Department has also introduced two AC Deluxe bus at Havelock Island for local transportation. There are also seaplanes and helicopter services between Havelock and Port Blair.
    </p>
    <p align="left">
    The Island of Havelock is home to scenic beaches like Radhanagar, Vijaynagar, Elephant and Kalapather, which are the top attractions of Andaman. Amongst the best things to do in Havelock Island are beach combing, sunbathing, scuba diving, sea walk, snorkeling, surfing, swimming, game fishing, watching the sunset or sunrise, bird-watching, trekking, sailing, and kayaking.October to February is the best time to visit Andaman. Mainly honeymooners can be seen visiting the island during this time. The temperature ranges between 15 ⁰C and 34 ⁰C during these months. On the other side, the months of March and April are part of the summer season in Havelock Island are ideal to enjoy various water sports like snorkelling, diving etc.
    </p>
