    <h4 align="center">SUKHNA LAKE</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Sukhna_Lake" target="_blank">
    <img src="sukhna.jpg" align="right" alt=""></a>
</p>
    <p align="left">
    The manmade picturesque Sukhna lake in the foothills of Shivalik range is an idyllic place for quiet communion with nature and for offering water sports activities such as boating, yachting, water skiing, etc. The creation of Lake was one of the greatest gift from Le Corbusier and P L Varma Chief Engineer. To preserve its tranquility Corbusier insisted that it be forbidden to motor boats and the top of the dam (promenade) prohibited to vehicular traffic. </p>
    <p align="left">Sukhna is an inseparable part of the city. Le Corbusier had foreseen that the residents of the city would be drawn it for the 'care of the body and spirit'. The planners of the city will be thrilled to find that this beautiful gift to the city has extended its attraction beyond its boundaries. The city planners were deeply attached to the lake. So much so that Pierre Jeanneret's ashes were immersed in the lake in 1970 as per his wishes by his niece.This 3 sq km rainfed lake was created in 1958 by damming the Sukhna Choe, a seasonal stream coming down from the Shivalik Hills. Originally the seasonal flow entered the lake directly causing heavy siltation. To check the inflow of silt, land measuring 2542 hectares was acquired in the catchment area and put under vegetation. In 1974, the Choe was diverted and made to bye-pass the lake completely, the lake being fed by three siltation pots, minimising the entry of silt into the lake itself.
    </p>
    <p align="left">
    The roof of the 'bund' or dam, elegantly landscaped has become a favourite promenade. Serious 'walkers' pursue an exercise regime, families enjoy an evening stroll and nature lovers mingle with children on roller skates - to partake of this extraordinary amenity. Photographers and painters love to capture its scenic beauty of the setting sun, or the heavily clouded monsoon sky, or the early morning mist in winter set amidst the tranquility of the lake. Even anglers do not leave unrewarded.
    </p>
    <p align="left">
    Sukhna is a sanctuary for many exotic migratory birds like the Siberian duck, Storks and Cranes, during the winter months. The Lake has been declared as a protected national wetland by the Government of India. People of Chandigarh love Sukhna. During summers, there are streams of men, women and children from all walks of life offering voluntary service to desilt the lake bed for about three months. This annual ritual has been a regular feature since long.Sukhna Lake is the venue for many festive celebrations too. The most popular is the Mango Festival held during the monsoons when scores of varieties of mangoes are on display. From time-to-time other food festivals, featuring specialities from different Indian States, are also held here, alongwith cultural performances.
    </p>
