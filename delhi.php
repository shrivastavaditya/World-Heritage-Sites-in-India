    <h4 align="center">RED FORT</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Red_Fort" target="_blank">
    <img src="redfort.jpg" height="300px" align="right" alt=""></a>
</p>
    <p align="left">
    Built out of red sandstone, Red Fort or Lal Quila was initially a residential area of the Mughals for about 200 years. The place was once used to celebrate various ceremonies and have now become one of the major tourist attractions in Delhi. Red Fort was even used to perform various ceremonies.
    </p>
    <p align="left">The fort’s massive wall standing at a height of 75 feet (23 metres) high, comprises a complex of palaces and entertainment halls, projecting balconies, baths and indoor canals, and geometrical gardens, as well as an ornate mosque. Visiting Red Fort, visitors come across the most famous structures of the complex, i.e., Diwan-i-Am, consisting of 60 red sandstone pillars supporting a flat roof, Diwan-i-Khas which is smaller, with a pavilion of white marble.Today the Red Fort is considered to be representing the zenith of the Mughal creativity. On visiting the tourist spot, one could witness the Islamic prototypes and each pavilion reveals architectural elements typical of Mughal building. Last but not the least, it is the innovative planning and and beautiful architecture including the garden style which make Red Fort a must visit tourist attraction in Delhi.
    </p>
    <p align="left">
    The Red Fort remains open all throughout the week except Mondays. The Red Fort from 7:00 AM to 5:30 PM.The Red Fort can be visited at any time of the year but winter season between November and February can be considered for making the most out of visit to Red Fort.
    </p>
    <p>
        <b>HISTORY</b>
        <br>
        The construction of the Red Fort began in the sacred month of Muharram in the year 1638 under the supervision of Shah Jahan when he moved the capital of his empire from Agra to Delhi which he called Shahjahanabad. Moving to Delhi Shah Jahan laid the foundation of Red Fort taking nearly a decade to complete. For around 200 years, Red Fort was the seat of the Mughal empire until it fell into the British Hands. This fort was the seat of the Mughal empire for around 200 years, until it fell into British hands. Unlike other Mughal Forts, Red Fort has boundaries and walls that are asymmetrical.
    </p>
