<article>
    <h4 align="center">VISAKHAPATNAM</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Visakhapatnam" target="_blank">
    <img src="vish.jpg" align="right" width="600px" alt=""></a>
</p>
    <p align="left">
    Visakhapatnam, also commonly known as Vizag, is one of the oldest port cities in the country. Situated in the heart of Andhra Pradesh, Visakhapatnam is known for its picturesque beaches and serene landscape, as well as a rich cultural past. The port of Visakhapatnam is renowned for being home to the oldest shipyard in all of India. Located a short distance away from Visakhapatnam is Araku Valley, one of the prime attractions of Vizag. Situated at a lofty 910 metres above sea level, Araku Valley is a hill station replete with gushing waterfalls, crystal clear streams, lush green gardens and some challenging trekking trails. </p>
    <p align="left">Vizag is dotted with many beaches along its coastline, with the most famous one being the Yarada Beach. One of the most stunning places to see in all of Visakhapatnam, Yarada beach is surrounded by majestic hills on three sides, and by the Bay of Bengal on the other side. One of the best things about Vizag’s beaches is that they are a lot cleaner and a lot less crowded than other famous beaches along the Indian coastline, and Yarada beach is no exception. It is the perfect place to sit back on the golden sands and witness a marvellous sunrise or sunset. 
    </p>
    <p align="left">
    If you’re a history lover, the Borra Caves are one of the best tourist attractions that Vizag has to offer for you. Nestled amidst the Ananthagiri Hills in Araku Valley, the Borra Caves are over thousands of years old and are the biggest among all the caves found in India. Made entirely of natural karstic limestone, the caves are kaleidoscopic in nature, and the light falling on the rocks coupled with the waterfalls creates an extremely colourful effect, lending an aura of magic and mysticism to the entire ambience.
    </p>
    <p align="left">
    The Katiki Waterfalls, situated a short distance away from the Borra Caves, is a gushing waterfall that measures around 50 feet in height and is completely surrounded by the lush greenery of the valley. The crystal-clear water cascading down the rocks, bordered by the gleaming green foliage is a sight for sore eyes. Being a coastal area, the primary cuisine of Visakhapatnam includes seafood, as well as traditional Andhra South Indian cuisine.  While Visakhapatnam is not exactly renowned for being a shopping destination, you will still find a lot of things that you can take back home. You can purchase a wide variety of stuff starting from handicrafts to Kalamkari paintings to wooden toys to even gorgeous Pochampalli and Ikat sarees.
    </p>
