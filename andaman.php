    <h4 align="center">BARREN Island</h4>
    <p align="right">
        <a href="https://en.wikipedia.org/wiki/Barren_Island_(Andaman_Islands)" target="_blank">
    <img src="barren-island1.jpg" height="300px" align="right" alt=""></a>
</p>
    <p align="left">
    Barren Island as the name implies is completely lifeless with no living human, except some animals and birds like goats, bats, flying foxes, and rats. This Island has the only active volcano in India which erupted twice in the past years of 1991 and 1994. Since then, the volcano has erupted more than six times and the recent eruption that took place was on 2 May 2006. However, if you want to venture for a sightseeing tour around the Island, you are to get permits from the Forest Department. The island can be visited by rented boats/ferries but the visitors are restricted to land ashore. You can also take a Sea Plane tour around the island and back. </p>
    <p align="left">There are two ways to reach the Barren Island, you can either take a seaplane from Port Blair or take a ferry ride which will take three rounds around the Island and back.Barren Island is a barren land with nothing to do but you can go for a sightsee expedition via ferry to get up close with the Island and seaplane to get an aerial view of the Volcano.
    </p>
    <p align="left">
    November to February is considered the best months to visit Barren Island because the weather will be clear giving a chance to see the island and the volcano.
    </p>
